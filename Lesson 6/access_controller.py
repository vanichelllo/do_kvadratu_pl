# access_controller.py

# 1. Симуляція даних з бази даних платформи
student_balance_uah = 300.0
material_price_uah = 450.0
is_student_banned = False

print("=== СИСТЕМА АВТОРИЗАЦІЇ DO_KVADRATU ===")

# 2. Логічне розгалуження (Каскад перевірок)
# Найвищий пріоритет - перевірка на бан.
# Якщо is_student_banned дорівнює True, виконується цей блок.
if is_student_banned:
    # Зверніть увагу на відступ (4 пробіли) перед print
    print("[Помилка 403] Ваш обліковий запис деактивовано.")
    print("Доступ до бази матеріалів НМТ обмежено.")

# Якщо попередній if повернув False (учень не в бані), Python переходить сюди
elif student_balance_uah >= material_price_uah:
    # Перевіряємо, чи баланс більший або дорівнює ціні. Якщо True:
    new_balance = student_balance_uah - material_price_uah
    print("[Повідомлення 200] Оплата успішна. Завантаження PDF-файлу...")
    print(f"Ваш залишок: {new_balance} UAH.")

# Якщо ЖОДНА з попередніх умов не виконалася (не в бані, але грошей мало)
else:
    # Обчислюємо нестачу коштів
    deficit = material_price_uah - student_balance_uah
    print("[Повідомлення 402] Відмова. Недостатньо коштів на рахунку.")
    print(f"Для доступу до конспекту поповніть баланс на {deficit} UAH.")

# Цей код знаходиться без відступу (на нульовому рівні).
# Він не залежить від умов if/else і виконається завжди в самому кінці.
print("=== РОБОТУ СКРИПТА ЗАВЕРШЕНО ===")