# Підключаємо вбудовану бібліотеку для роботи з форматом JSON.
# Її не потрібно завантажувати через pip, вона є в Python за замовчуванням.
import json

# 1. СТВОРЕННЯ ДАНИХ (Пам'ять Python)
# Поки що ці дані існують лише в оперативній пам'яті (RAM).
# Якщо комп'ютер вимкнеться зараз — вони зникнуть.
config_data = {
    "site_name": "До Квадрату",
    "version": 1.0,
    "is_active": True
}

# 2. ЗАПИС У ФАЙЛ (Збереження на жорсткий диск)
# Конструкція 'with' називається "Менеджер контексту".
# Її головна суперсила: вона гарантує, що файл буде закрито, навіть якщо під час запису станеться помилка.
# Якби ми відкривали файл без with, нам би довелося писати file.close() вручну, інакше файл міг би заблокуватися системою.
#
# Параметри функції open():
# - "config.json": назва файлу (буде створено в тій самій папці, де лежить скрипт).
# - "w" (write): режим запису. УВАГА! Цей режим ПОВНІСТЮ СТИРАЄ старий вміст файлу перед записом.
# - encoding="utf-8": життєво необхідний параметр для української мови. Без нього Windows може зберегти файл у старому кодуванні, і ви отримаєте "ієрогліфи".
with open("config.json", "w", encoding="utf-8") as file:
    # json.dump() бере словник Python і конвертує його в текстовий формат JSON, одразу записуючи у відкритий файл.
    # Параметри json.dump():
    # - config_data: що саме записуємо.
    # - file: куди записуємо (наша змінна з open()).
    # - ensure_ascii=False: вимикає примусове перетворення кирилиці в коди. Без цього "До Квадрату" перетворилося б на "\u0414\u043e \u041a\u0432..."
    # - indent=4: додає красиві відступи (4 пробіли). Без цього весь JSON запишеться в один суцільний нечитабельний рядок.
    json.dump(config_data, file, ensure_ascii=False, indent=4)

    print("Дані успішно збережено у файл config.json!")

# 3. ЧИТАННЯ З ФАЙЛУ (Відновлення даних з жорсткого диска в пам'ять Python)
# Знову використовуємо with, щоб безпечно відкрити файл.
# - "r" (read): режим читання. Якщо файлу "config.json" не існує, Python видасть помилку (FileNotFoundError).
with open("config.json", "r", encoding="utf-8") as file:
    # json.load() зчитує весь текст із файлу і перетворює його назад на справжній словник Python.
    # Після цього ми знову можемо звертатися до даних через квадратні дужки, наприклад ['site_name'].
    loaded_data = json.load(file)

    print("--- Результат читання ---")
    print(f"Назва платформи: {loaded_data['site_name']}")
    print(f"Версія: {loaded_data['version']}")