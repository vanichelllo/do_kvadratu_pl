# lesson5_lists.py

# 1. Ініціалізація списку
# Створюємо масив рядків, який описує модулі курсу з математики
nmt_math_modules = ["Алгебра: База", "Рівняння та нерівності", "Планіметрія"]

print("=== ПОЧАТКОВИЙ СТАН ===")
print(f"Кількість модулів: {len(nmt_math_modules)}")
print(f"Перелік: {nmt_math_modules}")

# 2. Додавання елементів
# Метод .append() завжди додає новий елемент у самий кінець списку
nmt_math_modules.append("Стереометрія")

# Метод .insert() дозволяє вставити елемент у конкретне місце за індексом
# Вставляємо "Тригонометрію" на позицію з індексом 2 (між "Рівняння..." та "Планіметрією")
nmt_math_modules.insert(2, "Тригонометрія")

print("\n=== ПІСЛЯ ДОДАВАННЯ ===")
print(f"Оновлений перелік: {nmt_math_modules}")

# 3. Читання та зміна елементів за індексом
# Припустимо, ми хочемо перейменувати перший модуль (індекс 0)
nmt_math_modules[0] = "Числа і вирази (Алгебра)"

# Отримуємо останній елемент.
# У Python індекс [-1] завжди вказує на останній елемент, [-2] - на передостанній і т.д.
last_module = nmt_math_modules[-1]

print("\n=== ЗМІНА ТА ЧИТАННЯ ===")
print(f"Оновлений перший модуль: {nmt_math_modules[0]}")
print(f"Останній модуль у черзі: {last_module}")

# 4. Видалення елементів
# Метод .remove() шукає елемент за його значенням і видаляє перше співпадіння
nmt_math_modules.remove("Планіметрія")

# Метод .pop() видаляє елемент за індексом та повертає його (можна записати у змінну)
# Якщо викликати .pop() без аргументів, він видалить останній елемент
removed_module = nmt_math_modules.pop(1)  # Видаляємо "Рівняння та нерівності"

print("\n=== ПІСЛЯ ВИДАЛЕННЯ ===")
print(f"Видалено модуль: {removed_module}")
print(f"Фінальний стан списку: {nmt_math_modules}")

cohort_students = ["Іванов","Коваленко","Шевченко"]
cohort_students.append("Ткаченко")
cohort_students.remove("Коваленко")
cohort_students.insert(1,"Бойко")
print(f"Актуальний список групи: {cohort_students}. У групі навчається {len(cohort_students)} учні.")